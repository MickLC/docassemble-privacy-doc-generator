# Vendor DPA Gap Analysis (cross-jurisdictional)
# -----------------------------------------------
def analyse_vendor_dpa_gaps(matter):
    """
    GDPR Article 28, CCPA §1798.140(ag), TDPSA §541.104, and VCDPA
    §59.1-580 each require a DPA with processors/service providers.
    Checked once per vendor against matter.systems rather than once
    per jurisdiction against a duplicative global posture flag, so a
    vendor missing a DPA produces a single finding citing every
    applicable jurisdiction's authority instead of one near-duplicate
    finding per jurisdiction.
    """
    findings = []
    confirmed = matter.confirmed_jurisdictions.true_values()

    authorities = []
    if 'GDPR' in confirmed:
        authorities.append('Article 28 GDPR')
    if 'CCPA/CPRA' in confirmed:
        authorities.append('Cal. Civ. Code §1798.140(ag)')
    if 'TDPSA' in confirmed:
        authorities.append('Tex. Bus. & Com. Code §541.104')
    if 'VCDPA' in confirmed:
        authorities.append('Va. Code Ann. §59.1-580')

    if not authorities:
        return findings

    jurisdiction_label = ' / '.join(
        j for j in ('GDPR', 'CCPA/CPRA', 'TDPSA', 'VCDPA') if j in confirmed
    )

    for vendor in matter.systems:
        if not getattr(vendor, 'dpa_in_place', False):
            findings.append(_gap(
                jurisdiction_label,
                'Data Processing Agreement — {}'.format(vendor.name),
                '; '.join(authorities),
                'No Data Processing Agreement in place with "{}".'.format(
                    vendor.name
                ),
                'The client does not have a compliant Data Processing '
                'Agreement in place with "{}". A DPA is required wherever '
                'this vendor processes personal data on the client\'s '
                'behalf.'.format(vendor.name),
                '5. Vendor & DPA Management',
                'A missing DPA leaves the client without a binding '
                "contractual basis for a vendor's handling of personal "
                'data, and is independently required across every '
                'jurisdiction that applies to this matter.',
                MUST_FIX
            ))

    return findings


