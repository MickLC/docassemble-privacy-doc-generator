# Security Measures Gap Analysis (cross-jurisdictional)
# -----------------------------------------------
def analyse_security_measures_gaps(matter):
    """
    Replaces the old GDPR-only Privacy by Design check with a
    per-activity check against the security_measures already gathered
    in processing_activities.yml for every activity regardless of
    jurisdiction. Cites GDPR Article 32 when GDPR applies, otherwise a
    general best-practice framing.
    """
    findings = []
    gdpr_applies = 'GDPR' in matter.confirmed_jurisdictions.true_values()
    J = 'GDPR' if gdpr_applies else 'General'
    authority = (
        'Article 32 GDPR' if gdpr_applies
        else 'General security best practice'
    )

    for activity in matter.processing_activities:
        if getattr(activity, 'no_security_measures', False):
            findings.append(_gap(
                J,
                'Security Measures — {}'.format(activity.name),
                authority,
                'No security measures selected for the "{}" processing '
                'activity.'.format(activity.name),
                'The client has not identified any technical or '
                'organizational security measures (encryption, access '
                'controls, logging, etc.) protecting the personal data '
                'processed in the "{}" activity.'.format(activity.name),
                '7. Security Program',
                'Data with no identified technical or organizational '
                'protections is the first thing a regulator or plaintiff\'s '
                'attorney will point to after an incident.',
                SHOULD_FIX
            ))

    return findings


