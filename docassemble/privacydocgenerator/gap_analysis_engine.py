# Staff Training Gap Analysis (cross-jurisdictional)
# -----------------------------------------------
def analyse_staff_training_gaps(matter):
    """
    Consolidates GDPR Article 39(1)(b)'s staff awareness-raising/
    training obligation and CCPA/CPRA's staff-training requirement
    (§1798.135(a)(3)) into a single check against one matter-level
    flag (matters[i].playbooks.staff_trained) instead of two
    near-duplicate per-jurisdiction posture questions asking about the
    same underlying practice.
    """
    findings = []
    confirmed = matter.confirmed_jurisdictions.true_values()

    authorities = []
    if 'GDPR' in confirmed:
        authorities.append('Article 5(2) GDPR (accountability); Article 39(1)(b)')
    if 'CCPA/CPRA' in confirmed:
        authorities.append('Cal. Civ. Code §1798.135(a)(3)')

    if not authorities:
        return findings

    jurisdiction_label = ' / '.join(
        j for j in ('GDPR', 'CCPA/CPRA') if j in confirmed
    )

    if not matter.playbooks.staff_trained:
        findings.append(_gap(
            jurisdiction_label,
            'Staff Data Protection Training',
            '; '.join(authorities),
            'Staff have not received data protection training in the last '
            '12 months.',
            'No evidence of current staff training on data protection '
            'obligations, including handling of data subject/consumer '
            'rights requests. Regular training is a key element of the '
            'accountability principle and a DPO obligation where a DPO is '
            'appointed.',
            '8. Staff Training',
            'Untrained staff are the most common point of failure in an '
            'otherwise well-designed privacy program — policies and '
            'procedures only work if the people executing them know what '
            'they are.',
            SHOULD_FIX
        ))

    return findings


