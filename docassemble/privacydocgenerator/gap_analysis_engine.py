# DPIA/LIA Substantive Gap Analysis (GDPR)
# -----------------------------------------------
def analyse_dpia_lia_gaps(matter):
    """
    DPIA/LIA completion itself is mandatory in the interview flow
    (see dpia_assessment.yml / lia_assessment.yml, both referenced in
    main.yml's required sequence), so "was it done at all" can never
    be false by the time gap analysis runs. What's actually worth
    flagging is a *completed* assessment that concluded something
    concerning: a DPIA risk register entry left at "High — processing
    should not proceed without further mitigation," or an LIA
    conclusion of "No" or "Undetermined."
    """
    findings = []
    if 'GDPR' not in matter.confirmed_jurisdictions.true_values():
        return findings

    for activity in matter.processing_activities:
        if activity.is_high_risk:
            for risk in activity.dpia.risks:
                if risk.residual_outcome == (
                    'High — processing should not proceed without '
                    'further mitigation'
                ):
                    findings.append(_gap(
                        'GDPR',
                        'Unresolved DPIA Risk — {}'.format(activity.name),
                        'Article 35 GDPR',
                        'DPIA risk register for "{}" leaves a risk at '
                        '"High — should not proceed" without further '
                        'mitigation.'.format(activity.name),
                        'The client\'s own Data Protection Impact Assessment '
                        'for the "{}" processing activity identified a risk '
                        '("{}") whose residual outcome after mitigation is '
                        'still rated High, meaning the assessment itself '
                        'concludes this processing should not proceed '
                        'without further mitigation.'.format(
                            activity.name, risk.description
                        ),
                        '9. Assessments (DPIA/LIA)',
                        "The client's own risk assessment concluded this "
                        "processing shouldn't proceed without further "
                        "mitigation — continuing without addressing it is "
                        "processing against the client's own documented "
                        "judgment.",
                        MUST_FIX
                    ))

        if activity.relies_on_legitimate_interest:
            conclusion = activity.lia.conclusion
            if conclusion == 'No — an alternative lawful basis should be used':
                findings.append(_gap(
                    'GDPR',
                    'LIA Conclusion — {}'.format(activity.name),
                    'Article 6(1)(f) GDPR',
                    'LIA for "{}" concluded the legitimate interests basis '
                    'is not supported.'.format(activity.name),
                    'The client\'s own Legitimate Interests Assessment for '
                    'the "{}" processing activity concluded that the '
                    'legitimate interests basis does not survive the '
                    'balancing test, but the activity still relies on it as '
                    'its lawful basis.'.format(activity.name),
                    '9. Assessments (DPIA/LIA)',
                    "The client's own balancing test concluded the "
                    "legitimate interests basis doesn't hold, but the "
                    'activity still relies on it — this is processing '
                    "without a valid lawful basis by the client's own "
                    'analysis.',
                    MUST_FIX
                ))
            elif conclusion == 'Undetermined — needs further review':
                findings.append(_gap(
                    'GDPR',
                    'LIA Conclusion — {}'.format(activity.name),
                    'Article 6(1)(f) GDPR',
                    'LIA for "{}" remains undetermined.'.format(activity.name),
                    'The client\'s Legitimate Interests Assessment for the '
                    '"{}" processing activity was left undetermined and '
                    'needs further review before the legitimate interests '
                    'basis can be relied upon with confidence.'.format(
                        activity.name
                    ),
                    '9. Assessments (DPIA/LIA)',
                    'An unresolved LIA leaves the activity\'s lawful basis '
                    'unconfirmed — it needs to be closed out, not left open '
                    'indefinitely.',
                    SHOULD_FIX
                ))

    return findings


# -----------------------------------------------
# Master gap analysis function (called from YAML)
# -----------------------------------------------
def run_gap_analysis(matter):
    """
    Runs all applicable gap analysis functions for the matter
    and returns a consolidated findings structure:

    {
      'all': [list of all findings],
      'by_jurisdiction': { 'GDPR': [...], 'CCPA/CPRA': [...], ... },
      'by_severity': {
          'Must Fix': [...],
          'Should Fix': [...],
          'Consider Fixing': [...]
      },
      'counts': {
          'total': int,
          'must_fix': int,
          'should_fix': int,
          'consider': int
      }
    }
    """
    confirmed = matter.confirmed_jurisdictions.true_values()
    all_findings = []

    if 'GDPR' in confirmed:
        all_findings.extend(analyse_gdpr_gaps(matter))

    if 'CCPA/CPRA' in confirmed:
        all_findings.extend(analyse_ccpa_gaps(matter))

    if 'TDPSA' in confirmed:
        all_findings.extend(analyse_tdpsa_gaps(matter))

    if 'VCDPA' in confirmed:
        all_findings.extend(analyse_vcdpa_gaps(matter))

    all_findings.extend(analyse_vendor_dpa_gaps(matter))
    all_findings.extend(analyse_breach_readiness_gaps(matter))
    all_findings.extend(analyse_security_measures_gaps(matter))
    all_findings.extend(analyse_staff_training_gaps(matter))
    all_findings.extend(analyse_dpia_lia_gaps(matter))

    by_jurisdiction = {}
    for f in all_findings:
        j = f['jurisdiction']
        by_jurisdiction.setdefault(j, []).append(f)

    by_severity = {
        MUST_FIX: [f for f in all_findings if f['severity'] == MUST_FIX],
        SHOULD_FIX: [f for f in all_findings if f['severity'] == SHOULD_FIX],
        CONSIDER: [f for f in all_findings if f['severity'] == CONSIDER],
    }

    counts = {
        'total': len(all_findings),
        'must_fix': len(by_severity[MUST_FIX]),
        'should_fix': len(by_severity[SHOULD_FIX]),
        'consider': len(by_severity[CONSIDER]),
    }

    return {
        'all': all_findings,
        'by_jurisdiction': by_jurisdiction,
        'by_severity': by_severity,
        'counts': counts,
    }
