# Breach Readiness Gap Analysis (cross-jurisdictional)
# -----------------------------------------------
def analyse_breach_readiness_gaps(matter):
    """
    Breach-response organizational readiness (internal escalation
    contact, counsel/forensics firm on retainer) is gathered once per
    matter in playbooks.yml regardless of which privacy-law
    jurisdictions apply — it isn't GDPR-specific even though Article
    33/34 GDPR is the only statutory citation this codebase currently
    ties to it. Runs for every matter rather than being scoped inside
    analyse_gdpr_gaps, so a US-only matter with no EU exposure still
    gets flagged for having no one designated to handle a breach.
    """
    findings = []
    pb = matter.playbooks
    gdpr_applies = 'GDPR' in matter.confirmed_jurisdictions.true_values()
    J = 'GDPR' if gdpr_applies else 'General'
    authority = (
        'Articles 33 and 34 GDPR' if gdpr_applies
        else 'General breach-response best practice'
    )

    if not pb.breach_escalation_contact:
        findings.append(_gap(
            J,
            'Data Breach Response Procedure',
            authority,
            'No internal escalation contact designated for a suspected breach.',
            'The client has not designated an internal contact responsible '
            'for detecting, assessing, and escalating a personal data '
            'breach. Without this, there is no functioning breach response '
            'procedure regardless of what documentation may say.',
            '6. Breach Response',
            'Without someone designated to receive and act on a suspected '
            'breach, statutory notification clocks can run out before '
            'anyone even starts responding.',
            MUST_FIX if gdpr_applies else SHOULD_FIX
        ))

    if not pb.breach_counsel_retained:
        findings.append(_gap(
            J,
            'Breach Counsel / Forensics Firm on Retainer',
            authority,
            'No breach counsel or forensics firm retained.',
            'The client has not retained breach counsel or a forensics firm '
            'in advance. Identifying and engaging one during an active '
            'breach costs valuable time against strict statutory '
            'notification deadlines.',
            '6. Breach Response',
            'Identifying and engaging breach counsel or a forensics firm '
            "during an active incident wastes time the client doesn't have "
            'against strict notification deadlines.',
            CONSIDER
        ))

    return findings


